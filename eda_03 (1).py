"""Correlation Heatmap

We identified the top 10 features most correlated with SalePrice and visualized them in a heatmap.
This analysis helps determine which variables have the strongest influence on the price.
From the plot, we can see that features like Gr Liv Area and Overall Qual have a major impact on house value.

Scatter Plot
"""

def correlation_heatmap(df, target="SalePrice", top_n=10):
    import numpy as np
    import seaborn as sns
    import matplotlib.pyplot as plt

    numeric_df = df.select_dtypes(include=np.number)

    corr_matrix = numeric_df.corr()


    if target in corr_matrix.columns:
        top_corr = corr_matrix[target].abs().sort_values(ascending=False).head(top_n+1)

        top_features = top_corr.index
        top_corr_matrix = corr_matrix.loc[top_features, top_features]

        plt.figure(figsize=(8,6))
        sns.heatmap(top_corr_matrix, annot=True, cmap="coolwarm")
        plt.title(f"Top {top_n} Features Correlated with {target}")
        plt.show()

        print(f"Insight: The most correlated features with {target} are "
              f"{', '.join(top_features[1:])}. "
              "These features likely have strong influence on the target.\n")

    else:
        print(f"Target '{target}' is not numeric, cannot compute correlations.\n")

