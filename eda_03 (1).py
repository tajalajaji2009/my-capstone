"""Histograms / KDE

We plotted the distribution of SalePrice, Gr Liv Area, and Overall Qual using histograms and KDE.
The plots show whether the distribution is skewed or approximately symmetric.
We can observe that most values cluster around the mean, while some extreme values represent outliers that could influence statistical analysis.
"""

def plot_histograms(df, features=["SalePrice", "Gr Liv Area", "Overall Qual"]):
    for col in features:
        plt.figure(figsize=(6,4))
        sns.histplot(df[col], kde=True)
        plt.title(f"Distribution of {col}")
        plt.show()
        skew = df[col].skew()
        mean = df[col].mean()
        print(f"{col} - Skew: {skew:.2f}, Mean: {mean:.2f}")
        if skew > 1:
            shape = "right-skewed"
        elif skew < -1:
            shape = "left-skewed"
        else:
            shape = "approximately symmetric"
        print(f"Insight: The distribution of {col} is {shape}, which suggests that most houses have {col.lower()} around the mean ({mean:.2f}). Outliers may exist on the extremes.\n")

