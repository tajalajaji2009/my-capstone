"""Manual Mean and Std

We manually calculated the mean and standard deviation of SalePrice without using pandas built-in functions.
This helps in understanding how basic statistics are computed step by step.
These values can be used later for normalization or comparing the variation in house prices.
"""

def manual_mean_std(y):
    y_array = np.array(y)
    mean = np.sum(y_array)/len(y_array)
    std = np.sqrt(np.sum((y_array - mean)**2)/len(y_array))
    print(f"Manual Mean: {mean:.2f}, Std: {std:.2f}")
    return mean, std

"""Standardize Column Manually

We standardized Gr Liv Area manually and calculated the Z-score for each value.
The results were compared with sklearn’s StandardScaler and matched perfectly.
This confirms that manual standardization is accurate and illustrates the process of data normalization.
"""

def standardize_column_manual(df, col):
    mean, std = manual_mean_std(df[col])
    z_manual = (df[col] - mean)/std
    print(f"First 5 manual standardized values:\n{z_manual.head()}")

    scaler = StandardScaler()
    z_sklearn = scaler.fit_transform(df[[col]]).flatten()
    print(f"First 5 sklearn standardized values:\n{z_sklearn[:5]}")

    comparison = np.allclose(z_manual, z_sklearn)
    print(f"\nDo manual Z and sklearn Z match? {comparison}\n")
    return z_manual, z_sklearn

"""Cosine Similarity

We measured the similarity between the highest and lowest SalePrice using cosine similarity.
This indicates how close or distant the numeric features are between the most expensive and least expensive houses.
It helps understand the variation in feature values across extreme price points.
"""

def cosine_high_low(df):
    numeric_df = df.select_dtypes(include=np.number)
    high_idx = df["SalePrice"].idxmax()
    low_idx = df["SalePrice"].idxmin()
    vec_high = numeric_df.loc[high_idx].values.reshape(1,-1)
    vec_low = numeric_df.loc[low_idx].values.reshape(1,-1)
    cos_sim = cosine_similarity(vec_high, vec_low)[0][0]
    print(f"Cosine similarity between highest and lowest SalePrice: {cos_sim:.2f}\n")
    return cos_sim

"""Estimate Probability

We estimated the fraction of high-quality houses (Overall Qual ≥7) that have SalePrice above 300,000.
The result shows that most high-quality houses are also high-priced, highlighting the impact of quality on price.
This quantitative analysis demonstrates the correlation between house quality and market value.
"""

def estimate_probability(df, quality_col="Overall Qual", threshold_price=300000, quality_threshold=7):
    high_quality = df[df[quality_col] >= quality_threshold]
    prob = (high_quality["SalePrice"] > threshold_price).mean()
    print(f"Fraction of high-quality items (Overall Qual >= {quality_threshold}) with SalePrice > {threshold_price}: {prob:.2f}")
    print(f"Insight: About {prob*100:.1f}% of high-quality houses are priced above ${threshold_price}. This shows high-quality homes tend to command higher prices.\n")
    return prob

