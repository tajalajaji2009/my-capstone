def show_head(df):
    return df.head(5)

"""Check the shape of the dataset"""

def get_shape(df):
    return df.shape

"""Inspect data types and fix wrong types"""

def get_info(df):
    df.info()

def fix_data_types(df):
    df["MS SubClass"] = df["MS SubClass"].astype(str)
    df["Neighborhood"] = df["Neighborhood"].astype(str).str.lower().str.strip()
    return df

"""Find missing values

I handled the missing values differently depending on the type of column:


* For numeric columns, I filled the
missing values with the median to avoid the effect of outliers.

* For categorical columns, I filled the missing values with "Unknown" to keep all rows and indicate which values were missing.  List item.

"""

def handle_missing_values(df):

    numeric_cols = df.select_dtypes(include=np.number).columns
    df[numeric_cols] = df[numeric_cols].fillna(df[numeric_cols].median())

    categorical_cols = df.select_dtypes(include="object").columns
    df[categorical_cols] = df[categorical_cols].fillna("Unknown")

    return df

"""Handle duplicates"""

def remove_duplicates(df):
    duplicates_count = df[df.duplicated()].shape[0]
    print(f"Found {duplicates_count} duplicate rows.")
    df_cleaned = df.drop_duplicates(ignore_index=True)
    return df_cleaned

"""Spot outliers"""

import matplotlib.pyplot as plt

