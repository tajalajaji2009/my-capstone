def handle_outliers(df):
    if "SalePrice" in df.columns:
        upper_limit = df["SalePrice"].quantile(0.99)
        df["SalePrice"] = df["SalePrice"].clip(upper=upper_limit)
    return df

"""data cleaning and validation function

"""

df = load_data("/kaggle/input/ames-housing-dataset/AmesHousing.csv")
show_head(df)

def clean_data(path):
    df = load_data(path)
    show_head(df)

    print("Shape before cleaning:", df.shape)

    df = fix_data_types(df)
    df = handle_missing_values(df)
    df = remove_duplicates(df)
    df = handle_outliers(df)

    print("Shape after cleaning:", df.shape)

    assert df.isnull().sum().sum() == 0
    assert df.duplicated().sum() == 0
    assert (df["SalePrice"] > 0).all()

    return df

cleaned_df = clean_data("/kaggle/input/ames-housing-dataset/AmesHousing.csv")